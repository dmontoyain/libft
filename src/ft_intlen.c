#include "libft.h"

size_t		ft_intlen(int nb)
{
	size_t		size;

	size = 0;
	if (nb < 0)
	{
		size++;
		nb = -nb;
	}
	while (nb >= 10)
	{
		nb = nb / 10;
		size++;
	}
	return (size + 1);
}
