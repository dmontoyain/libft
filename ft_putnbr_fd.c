#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char *ns;

	if (nb == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else
	{
		ns = ft_itoa(nb);
		ft_putstr_fd(ns, fd);
	}
}
